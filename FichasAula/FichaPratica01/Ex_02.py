# Ler num1
num1 = int(input("Introduza um número: "))

# ler num2
num2 = int(input("Introduza outro número: "))

# Calcular e apresentar a soma
resultado = num1 + num2
print("Soma: ", resultado)

# Calcular e apresentar a subtração
resultado = num1 - num2
print("Subtração: ", resultado)

# Calcular e apresentar a multiplicação
resultado = num1 * num2
print("Multiplicação: ", resultado)

# Calcular e apresentar a divisão
resultado = num1 / num2
print("Divisão: ", resultado)