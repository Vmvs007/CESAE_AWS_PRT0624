# Ler 3 valores
num1=float(input("Insira 1º número: "))
num2=float(input("Insira 2º número: "))
num3=float(input("Insira 3º número: "))

# Calcular e apresentar a media aritmetica
mediaAritmetica = (num1+num2+num3)/3
print("Média Aritmética:",mediaAritmetica)

# Calcular e apresentar a media ponderada
mediaPonderada = num1*0.2 + num2*0.3 + num3*0.5
print("Média Ponderada:",mediaPonderada)

